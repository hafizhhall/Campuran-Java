package com.Latihan;

public class Bersama {
    public static void main(String[] args) {
        for (int i = 5; i >= 1; i--) {
            System.out.print(" " + i);
        }
        System.out.println();

        for (int i = 10; i >=4; i-=2) {
            System.out.print(" " + i);
        }
        System.out.println();

        for (int i = 15; i >=9; i-=3) {
            System.out.print(" " + i);
        }
        System.out.println();

        for (int i = 20; i >=16; i-=4) {
            System.out.print(" " + i);
        }
        System.out.println();

        for (int i = 25; i >=25; i-=5) {
            System.out.print(" " + i);
        }
        System.out.println();
    }
}
